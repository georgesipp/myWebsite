		<h1 class="content-center px-8 mt-8 text-4xl font-bold">Playing Emissions</h1>
		<div class="~xs:~px-2/4 mt-8 ~xs:~text-xs/xm text-pretty text-justify">
			<p class="mb-4 font-bold">Premiere 09.06.2023, bat Studiotheater</p>
			<p class="mb-4">
				Treten Sie ein in den Themenpark der Luftzukünfte! Erleben sie die Produktion von Dampf,
				Körperwärme, Schweiss, Gefühlen, Atemübungen in verschiedenen ereignishaften Dramaturgien.
				Wie verwandeln wir starke persönliche Emotionen von Krisenmomenten in neue Formen des
				kollaborativen Spielens?
			</p>
			<p>Workstation: Hass</p>
			<p class="mb-4">
				Dieser Teil der Ausstellung fragt nach der Art und Weise, wie Klimadiskurse geführt werden.
				Drei Gestalten sprechen über das “Haus”, ein umweltschädliches Gebäude, das ihrer Meinung
				nach trotzdem eine Daseinsberechtigung hat. Die durch KI übermalten Figuren tragen ihre
				Argumente vor, so wie wir es schon hunderte Male gesehen haben. Sie fabulieren vor sich hin,
				ihre Forderungen bergen keinerlei utopische Vorstellungskraft. Bei uns im Raum befindet sich
				das “Haus”. Eine Betonskulptur. Das Werkzeug zum Vandalieren liegt bereit: Graffiti und
				Hammer.
			</p>
			<p class="mb-4">
				Wie lässt sich die entstehende Wut emittieren? In den direkten Diskurs können wir nicht
				treten. Müssen wir das “Haus” selbst niederreißen? Können wir den Hass produktiv machen,
				ohne die Selbstzerstörung zu fördern?
			</p>
			<p class="mb-4">
				Playing Emissions wurde in Kollaboration mit dem Experimentallabor »CollActive Materials«,
				dem Exzellenzcluster »Matters of Activity. Image Space Material« der Humboldt Universität zu
				Berlin und dem Potsdam Institut für Klimafolgenforschung entwickelt.
			</p>
			<p class="mb-4">
				eine interaktive Ausstellung von Mari Hakopyan, Jonas Olbrich, Vili Pääkö, Clemens Winkler
			</p>
